package admin;

public class ColumnWidth {

    private String columnIdentifier;
    private int columnWidth;
    private int columnIndex;
    private int columnModelIndex;

    public String getColumnIdentifier() {
        return columnIdentifier;
    }

    public void setColumnIdentifier(String columnIdentifier) {
        this.columnIdentifier = columnIdentifier;
    }

    public int getColumnWidth() {
        return columnWidth;
    }

    public void setColumnWidth(int columnWidth) {
        this.columnWidth = columnWidth;
    }

    public int getColumnIndex() {
        return columnIndex;
    }

    public void setColumnIndex(int columnIndex) {
        this.columnIndex = columnIndex;
    }

    public int getColumnModelIndex() {
        return columnModelIndex;
    }

    public void setColumnModelIndex(int columnModelIndex) {
        this.columnModelIndex = columnModelIndex;
    }
    
    

    
}
